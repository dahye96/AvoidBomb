public class Position {
    float posX;
    float posY;

    public Position(float x, float y) {
        this.posX = x;
        this.posY = y;
    }

    public float getPosX() {
        return posX;
    }

    public void setPosX(float posX) {
        this.posX = posX;
    }

    public float getPosY() {
        return posY;
    }

    public void setPosY(float posY) {
        this.posY = posY;
    }
}
